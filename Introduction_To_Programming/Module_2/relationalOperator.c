#include <stdio.h>
int main() {

    // Relational Operator || Comparison Operator (in Python)
    int a = 10, b = 5;
    printf("%d\n", a > b);
    printf("%d\n", a >= b);
    printf("%d\n", a < b);
    printf("%d\n", a <= b);
    printf("%d\n", a == b);
    printf("%d\n", a != b);

    return 0;
}